def create_project_structure(project_name: str, base_dir: str | None = None) -> dict:
    """Create a pentest project folder structure under *base_dir*.

    Structure:
        <base_dir>/<project_name>/
            01-Reconnaissance/nmap/
            01-Reconnaissance/subdomains/
            01-Reconnaissance/screenshots/
            02-Exploitation/
            03-Post-Exploitation/privesc/
            03-Post-Exploitation/loot/
            04-Lateral-Movement/
            05-Reporting/findings/
            05-Reporting/evidence/
            Tools/
            Scans/
    """
    if base_dir is None:
        base_dir = str(Path.home() / "Desktop")
    root = Path(base_dir).expanduser().resolve() / project_name
    try:
        root.mkdir(parents=True, exist_ok=True)
        created: list[str] = []
        for folder, subs in _PENTEST_TEMPLATE.items():
            fpath = root / folder
            fpath.mkdir(exist_ok=True)
            created.append(str(fpath))
            if isinstance(subs, list):
                for sub in subs:
                    (fpath / sub).mkdir(parents=True, exist_ok=True)
                    created.append(str(fpath / sub))
        logger.info("Created project structure at %s", root)
        return {
            "success": True,
            "message": f"Created pentest project '{project_name}' at {root}.",
            "details": {"root": str(root), "folders": created},
        }
    except Exception as exc:
        logger.error("Failed to create project structure: %s", exc)
        return {"success": False, "message": f"Could not create project: {exc}"}


