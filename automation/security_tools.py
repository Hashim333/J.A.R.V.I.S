def open_tool_documentation(tool_name: str) -> dict:
    """Open the documentation URL for a security tool in the default browser."""
    key = tool_name.strip().casefold()
    url = _TOOL_DOCS.get(key)
    if not url:
        names = ", ".join(sorted(_TOOL_DOCS))
        return {
            "success": False,
            "message": f"No documentation URL for {tool_name!r}. Known tools: {names}",
        }
    try:
        import webbrowser
        webbrowser.open(url)
        return {"success": True, "message": f"Opened {tool_name} documentation.", "details": {"url": url}}
    except Exception as exc:
        return {"success": False, "message": f"Could not open documentation: {exc}"}
