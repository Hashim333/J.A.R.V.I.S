def create_pentest_report(client_name: str, project_name: str, output_dir: str | None = None) -> dict:
    """Generate a templated pentest report markdown file."""
    if output_dir is None:
        output_dir = str(Path.home() / "Desktop")
    dst = Path(output_dir).expanduser().resolve() / f"{project_name}_Pentest_Report.md"
    try:
        dst.parent.mkdir(parents=True, exist_ok=True)
        content = _REPORT_TEMPLATE.format(
            client_name=client_name or "[Client Name]",
            project_name=project_name or "[Project]",
            date=datetime.now().strftime("%Y-%m-%d"),
        )
        dst.write_text(content, encoding="utf-8")
        logger.info("Created pentest report at %s", dst)
        return {
            "success": True,
            "message": f"Created pentest report at {dst}.",
            "details": {"path": str(dst)},
        }
    except Exception as exc:
        return {"success": False, "message": f"Could not create report: {exc}"}


