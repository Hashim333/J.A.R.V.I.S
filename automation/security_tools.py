def organize_scan_results(source_dir: str, project_name: str, base_dir: str | None = None) -> dict:
    """Organise scan output files into the Scans folder of a project.

    Files are grouped by date (today's results get a 'YYYY-MM-DD' subfolder)
    and by tool name inferred from the filename or extension.
    """
    src = Path(source_dir).expanduser().resolve()
    if not src.is_dir():
        return {"success": False, "message": f"Source directory not found: {source_dir}"}

    if base_dir is None:
        base_dir = str(Path.home() / "Desktop")
    scans_dir = Path(base_dir).expanduser().resolve() / project_name / "Scans"
    try:
        scans_dir.mkdir(parents=True, exist_ok=True)
    except Exception as exc:
        return {"success": False, "message": f"Could not create scans directory: {exc}"}

    today = datetime.now().strftime("%Y-%m-%d")
    dest = scans_dir / today
    dest.mkdir(exist_ok=True)

    moved = 0
    scan_extensions = {".xml", ".nmap", ".gnmap", ".txt", ".csv", ".html", ".json", ".cap", ".pcap", ".pcapng"}
    for entry in src.iterdir():
        if entry.is_file() and entry.suffix.casefold() in scan_extensions:
            try:
                shutil.move(str(entry), str(dest / entry.name))
                moved += 1
            except Exception as exc:
                logger.warning("Could not move %s: %s", entry.name, exc)

    if moved:
        msg = f"Moved {moved} scan result(s) to {dest}."
        logger.info(msg)
        return {"success": True, "message": msg, "details": {"count": moved, "destination": str(dest)}}
    return {"success": True, "message": "No scan result files found to organise."}


